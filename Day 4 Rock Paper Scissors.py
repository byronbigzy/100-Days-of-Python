def game():
    clear()
    print("Select your hand:\n0 = Rock\n1 = Paper\n2 = Scissors")
    player_choice = int(input("Enter: "))

    print(f"You chose:\n{options[player_choice]}")

    computer_choice = random.randint(0,2)
    print(f"The computer chose:\n{options[computer_choice]}")

    # Game Logic
    result = (player_choice - computer_choice) % 3

    if result == 0:
        draw()
    elif result == 1:
        victory()
    else:
        defeat()

def victory():
    print("You Won!")
    restart()

def defeat():
    print("You Lose!")
    restart()

def draw():
    print("You Drew!")
    restart()

def restart():
    while True:
        choice = input("Would you like to play again? (Yes/No)\n")

        if choice.strip().lower() == "yes":
            game()
            break
        elif choice.strip().lower() == "no":
            print("Thank you for playing")
            break
        else:
            print("Sorry, that input wasn't regonized. Try again.\n")

